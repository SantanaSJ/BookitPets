package com.example.onlinehotelbookingsystem.model.view;

public class LabelValue {

    private String label;
    private Long value;

    public String getLabel() {
        return label;
    }

    public LabelValue setLabel(String label) {
        this.label = label;
        return this;
    }

    public Long getValue() {
        return value;
    }

    public LabelValue setValue(Long value) {
        this.value = value;
        return this;
    }
}
